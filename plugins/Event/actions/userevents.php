class UserEventsAction extends ApiPrivateAuthAction
{
    var $group    = null;
    var $profiles = null;

    /**
     * Take arguments for running
     *
     * @param array $args $_REQUEST args
     *
     * @return boolean success flag
     */
    protected function prepare(array $args=array())
    {
        parent::prepare($args);

        //$this->events    = Happening::getEvents($this->arg('id'));
        $this->events    = Happening::getAllVideoEvents();
        return true;
    }

    /**
     * Handle the request
     *
     * Show the members of the group
     *
     * @return void
     */
    protected function handle()
    {
        parent::handle();

        // XXX: RSS and Atom

        switch($this->format) {
        case 'json':
            $this->showJsonObjects($this->events);
            break;
        default:
            // TRANS: Client error displayed when coming across a non-supported API method.
            $this->clientError(_('API method not found.'), 404);
        }
    }



}
